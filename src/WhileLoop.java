public class WhileLoop {
    public static void main(String[] args) {
        int x = 1;
        while (x<=12) {
            if (x%2==0) {
                System.out.println(x);
            }
            x++;
        }

        for (int i=0;i<10;i++) {
            System.out.println(i);
        }
    }
}
